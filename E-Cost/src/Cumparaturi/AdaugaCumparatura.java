    private void ValidareCampuri() throws SQLException{
        if(jcbxCumparator.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Vă rugam selectaţi un cumpărător!");
        }
        else
            if(jcbxProdusCumparat.getSelectedIndex() == 0){
                JOptionPane.showMessageDialog(null, "Vă rugam să introduce-ţi produsul cumpărat!");
            }
            else
                    if(jcbxFolosit.getSelectedIndex() == 0){
                        JOptionPane.showMessageDialog(null, "Vă rugam selectaţi persoana care foloseste produsul !");
                    }
                    else
                        if(jDataCumparatura.getDate() == null){
                            JOptionPane.showMessageDialog(null, "Vă rugam selectaţi data cand a-ti cumparat produsul !");
                        }
                        else
                            if(jcbxcategorie.getSelectedIndex() == 0){
                                JOptionPane.showMessageDialog(null, "Vă rugam selectaţi categoria produsul !");
                            }
                            else 
                                if(jtfPret.getText().isEmpty()){
                                     JOptionPane.showMessageDialog(null, "Vă rugam introduceti pretul !");
                                }
                                
                                    else{
                                    ExtragereDate();
                                    InserareProdus();
                                    JOptionPane.showMessageDialog(null, "Datele au fost adaugate cu succes !");
                                    setVisible(false);
                                }
    }
    
    private void ExtragereDate(){
        ProdusNou.setCumparator(jcbxCumparator.getSelectedItem().toString());
        ProdusNou.setProdus_cumparat(jcbxProdusCumparat.getSelectedIndex());
        
        BigDecimal pret_produs = new BigDecimal(jtfPret.getText());
        ProdusNou.setPret_produs(pret_produs);
       
        ProdusNou.setPersoana_folosit(jcbxFolosit.getSelectedItem().toString());
        
        Date curent_Date = jDataCumparatura.getDate();
        SimpleDateFormat format = new SimpleDateFormat("yyyy/MM/dd");
        String date = format.format(curent_Date);
        ProdusNou.setData_cumparare(date);
        
        ProdusNou.setCategorie(jcbxcategorie.getSelectedItem().toString());
        
        ProdusNou.setMetoda_plata(jcbxMetodaPlata.getSelectedItem().toString());
        
    }
    
    private void InserareProdus() throws SQLException{
    String sql = "Insert into produse_cumparate(cumparator, produs_cumparat, pret_produs, folosit, data_cumparatura , categorie, metoda_plata) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?)";
                ps = connected.prepareStatement(sql);
                ps.setString(1, ProdusNou.getCumparator());
                ps.setInt(2, ProdusNou.getProdus_cumparat());
                ps.setBigDecimal(3, ProdusNou.getPret_produs());
                ps.setString(4, ProdusNou.getPersoana_folosit());
                ps.setString(5, ProdusNou.getData_cumparare());
                ps.setString(6, ProdusNou.getCategorie());
                ps.setString(7, ProdusNou.getMetoda_plata());
                ps.executeUpdate();
    }
    
