    private void CumparatorComboBox() throws SQLException{
            String cumparatorOfComboBox = "Select id, prenume from utilizatori";
            ps = connected.prepareStatement(cumparatorOfComboBox);
            rs = ps.executeQuery();
             while(rs.next()){
                int id = rs.getInt("id");
                String prenume =rs.getString("prenume");
                jcbxCumparator.insertItemAt(prenume, id);   
                jcbxFolosit.insertItemAt(prenume, id); 
            }
    }
    
    private void ProdusComboBox() throws SQLException{
        String sql = "Select p.id, p.denumire_produs, m.denumire from produse p left join magazine m on p.magazin = m.id";
        ps = connected.prepareStatement(sql);
            rs = ps.executeQuery();
             while(rs.next()){
                int id = rs.getInt("id");
                String denumire =rs.getString("denumire_produs");
                String denumire_magazin = rs.getString("m.denumire");
                String denumire_concat = denumire + " - " + denumire_magazin;
                jcbxProdusCumparat.insertItemAt(denumire_concat, id);  
            }
    }
    
    private void MetodaPlata() throws SQLException{
        String sql = "Select valoare_parametru from parametri where denumire_parametru = 'metoda_plata'";
        ps = connected.prepareStatement(sql);
            rs = ps.executeQuery();
                while(rs.next()){
                    String denumire = rs.getString("valoare_parametru");
                    jcbxMetodaPlata.addItem(denumire);
                }
    }
    
    
           
